-- ---------- Función para auto-actualizar updated_at ----------
CREATE OR REPLACE FUNCTION set_updated_at()
RETURNS TRIGGER AS $$
BEGIN
  NEW.updated_at = now();
  RETURN NEW;
END;
$$ LANGUAGE plpgsql;

-- ============================================================
--  PAQUETES  (migrada de Supabase)
-- ============================================================
CREATE TABLE IF NOT EXISTS packages (
  id                  BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  slug                TEXT UNIQUE,
  title               TEXT NOT NULL,
  destination         TEXT,
  country             TEXT,
  region              TEXT,
  description         TEXT,
  price               INTEGER      DEFAULT 0,
  original_price      INTEGER      DEFAULT 0,
  discount            INTEGER      DEFAULT 0,
  currency            TEXT         DEFAULT 'MXN',
  duration            TEXT,
  nights              INTEGER      DEFAULT 0,
  image               TEXT,
  gallery             JSONB        DEFAULT '[]'::jsonb,
  category            TEXT         DEFAULT 'aventura',
  rating              NUMERIC(2,1) DEFAULT 4.5,
  reviews_count       INTEGER      DEFAULT 0,
  includes            JSONB        DEFAULT '[]'::jsonb,
  highlights          JSONB        DEFAULT '[]'::jsonb,
  is_active           BOOLEAN      DEFAULT true,
  is_featured         BOOLEAN      DEFAULT false,
  order_index         INTEGER      DEFAULT 0,
  airline             TEXT,
  flight_type         TEXT         DEFAULT 'internacional',
  service_type        TEXT         DEFAULT 'paquete',
  persons             INTEGER      DEFAULT 2,
  hotel_name          TEXT,
  hotel_stars         INTEGER,
  room_type           TEXT,
  accommodation_type  TEXT,
  itinerary_pdf       TEXT,
  dates               TEXT,
  contact_whatsapp    TEXT,
  cancellation_policy TEXT,
  flight_includes     JSONB        DEFAULT '[]'::jsonb,
  transfers_included  JSONB        DEFAULT '[]'::jsonb,
  itinerary           JSONB        DEFAULT '[]'::jsonb,
  important_info      JSONB        DEFAULT '[]'::jsonb,
  terms_conditions    JSONB        DEFAULT '[]'::jsonb,
  payment_options     JSONB        DEFAULT '[]'::jsonb,
  created_at          TIMESTAMPTZ  DEFAULT now(),
  updated_at          TIMESTAMPTZ  DEFAULT now()
);
CREATE INDEX IF NOT EXISTS idx_packages_active   ON packages (is_active);
CREATE INDEX IF NOT EXISTS idx_packages_featured ON packages (is_featured);
CREATE INDEX IF NOT EXISTS idx_packages_order    ON packages (order_index);
DROP TRIGGER IF EXISTS trg_packages_updated ON packages;
CREATE TRIGGER trg_packages_updated BEFORE UPDATE ON packages
  FOR EACH ROW EXECUTE FUNCTION set_updated_at();

-- ============================================================
--  HISTORIAL DE CAMBIOS DE PAQUETES  (migrada de Supabase)
-- ============================================================
CREATE TABLE IF NOT EXISTS package_history (
  id            BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  package_id    BIGINT REFERENCES packages(id) ON DELETE CASCADE,
  field_changed TEXT,
  old_value     TEXT,
  new_value     TEXT,
  changed_by    TEXT,
  changed_at    TIMESTAMPTZ DEFAULT now()
);
CREATE INDEX IF NOT EXISTS idx_history_package ON package_history (package_id);

