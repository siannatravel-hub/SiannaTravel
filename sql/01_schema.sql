-- ============================================================
--  SOLICITUDES DE MENSAJE / LEADS  (NUEVA)
--  Reemplaza las hojas "Tickets" + handoff. 1 fila por número (estado del contacto).
-- ============================================================
CREATE TABLE IF NOT EXISTS solicitudes (
  id                 BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  numero_whatsapp    TEXT UNIQUE,           -- clave de match (igual que el bot)
  nombre_cliente     TEXT,
  correo             TEXT,
  origen             TEXT    DEFAULT 'WhatsApp',
  numero_negocio     TEXT,
  tipo_servicio      TEXT,
  descripcion        TEXT,
  estado             TEXT    DEFAULT 'Nuevo',     -- Nuevo / Escalado / Atendido / Cerrado
  prioridad          TEXT    DEFAULT 'MEDIA',     -- BAJA / MEDIA / ALTA
  asesor             TEXT,                        -- Bianka / Gaby
  ultimo_mensaje     TEXT,
  seguimiento_enviado BOOLEAN DEFAULT false,
  bot_pausado        BOOLEAN DEFAULT false,       -- handoff: el bot se calla
  fecha_pausa_bot    TIMESTAMPTZ,
  intencion_compra   BOOLEAN DEFAULT false,
  fecha_hora         TIMESTAMPTZ DEFAULT now(),
  created_at         TIMESTAMPTZ DEFAULT now(),
  updated_at         TIMESTAMPTZ DEFAULT now()
);
CREATE INDEX IF NOT EXISTS idx_solicitudes_estado    ON solicitudes (estado);
CREATE INDEX IF NOT EXISTS idx_solicitudes_prioridad ON solicitudes (prioridad);
DROP TRIGGER IF EXISTS trg_solicitudes_updated ON solicitudes;
CREATE TRIGGER trg_solicitudes_updated BEFORE UPDATE ON solicitudes
  FOR EACH ROW EXECUTE FUNCTION set_updated_at();

